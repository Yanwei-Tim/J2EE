package com.yutong.axxc.parents.entity.msg;

import javax.xml.bind.annotation.XmlElement;


/**
 
 * @version $Revision 1.0 $ 2013-3-25 下午7:06:56
 */
public class ResultSendTtMsg {

	/**
	 * 车辆vin信息.
	 */
	@XmlElement(name = "vin")
	private String vin;

	/**
	 * 调度信息发送下发状态.
	 * 1：成功；0：失败
	 */
	@XmlElement(name = "flg")
	private int flg;

	/**
	 * @return Returns the vin.
	 */
	public String getVin() {
		return vin;
	}

	/**
	 * @param vin The vin to set.
	 */
	public void setVin(String vin) {
		this.vin = vin;
	}

	/**
	 * @return Returns the flg.
	 */
	public int getFlg() {
		return flg;
	}

	/**
	 * @param flg The flg to set.
	 */
	public void setFlg(int flg) {
		this.flg = flg;
	}

}
