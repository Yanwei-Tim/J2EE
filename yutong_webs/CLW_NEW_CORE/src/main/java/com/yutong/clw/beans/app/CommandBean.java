package com.yutong.clw.beans.app;

public class CommandBean {
	private String id	        ;//主键 流水
	private String sim_card_number	;//sim卡号
	private String vehicle_vin	;//车辆vin号
	private String msg_id		;//下发消息id(时间戳表示)
	private String send_command	;//下发命令字(bind命令、下发信息等)
	private String send_type	;//下发子类型(拍照、区域设置等)
	private String packet_content	;//发送给终端的数据包内容，参见“宇通车联网基础服务平台与应用通讯协议-vnap分册”
	private String deal_state	;//处理状态(0：待处理；1：下发基础平台；2：基础平台回应；3：下发终端成功)
	private String deal_time	;//处理时间
	private String send_times	;//发送次数，默认为0
	private String send_seq	;	//下发序列号
	private String remark		;//备注
	private String core_id		;//核心服务id
	private String operate_user_id	;//操作人
	private String operate_time	;//操作时间
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSim_card_number() {
		return sim_card_number;
	}
	public void setSim_card_number(String simCardNumber) {
		sim_card_number = simCardNumber;
	}
	public String getVehicle_vin() {
		return vehicle_vin;
	}
	public void setVehicle_vin(String vehicleVin) {
		vehicle_vin = vehicleVin;
	}
	public String getMsg_id() {
		return msg_id;
	}
	public void setMsg_id(String msgId) {
		msg_id = msgId;
	}
	public String getSend_command() {
		return send_command;
	}
	public void setSend_command(String sendCommand) {
		send_command = sendCommand;
	}
	public String getSend_type() {
		return send_type;
	}
	public void setSend_type(String sendType) {
		send_type = sendType;
	}
	public String getPacket_content() {
		return packet_content;
	}
	public void setPacket_content(String packetContent) {
		packet_content = packetContent;
	}
	public String getDeal_state() {
		return deal_state;
	}
	public void setDeal_state(String dealState) {
		deal_state = dealState;
	}
	public String getDeal_time() {
		return deal_time;
	}
	public void setDeal_time(String dealTime) {
		deal_time = dealTime;
	}
	public String getSend_times() {
		return send_times;
	}
	public void setSend_times(String sendTimes) {
		send_times = sendTimes;
	}
	public String getSend_seq() {
		return send_seq;
	}
	public void setSend_seq(String sendSeq) {
		send_seq = sendSeq;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getCore_id() {
		return core_id;
	}
	public void setCore_id(String coreId) {
		core_id = coreId;
	}
	public String getOperate_user_id() {
		return operate_user_id;
	}
	public void setOperate_user_id(String operateUserId) {
		operate_user_id = operateUserId;
	}
	public String getOperate_time() {
		return operate_time;
	}
	public void setOperate_time(String operateTime) {
		operate_time = operateTime;
	}

}
