package com.yutong.clw.beans.exceptions;

/**
 * 油耗统计业务异常类
 * @author zhanglizhi
 * @version 1.0 $ 2010-8-18 上午09:53:37
 */
public class VspeOilBusinessException extends RuntimeException{
    private static final long serialVersionUID = 1L;

    public VspeOilBusinessException() {
        super();
    } 

    /**
     * @param arg0
     */
    public VspeOilBusinessException(String message) {
        super(message);
    }

    /**
     * @param arg0
     */
    public VspeOilBusinessException(Throwable arg0) {
        super(arg0);
    }

    /**
     * @param arg0
     * @param arg1
     */
    public VspeOilBusinessException(String message, Throwable arg1) {
        super(message, arg1);
    }
}
