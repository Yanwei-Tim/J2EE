package com.neusoft.clw.check.domain;

public class SimpleRecord {
	private String vehicle_vin;
    private String longitude;
    private String latitude;
    private String direction;
    private String terminal_time;
    private String dingwei_stat;
    private boolean gpsIsExc;
    private String speeding;
    
	public String getSpeeding() {
		return speeding;
	}
	public void setSpeeding(String speeding) {
		this.speeding = speeding;
	}
	public boolean isGpsIsExc() {
		return gpsIsExc;
	}
	public void setGpsIsExc(boolean gpsIsExc) {
		this.gpsIsExc = gpsIsExc;
	}
	public String getVehicle_vin() {
		return vehicle_vin;
	}
	public void setVehicle_vin(String vehicle_vin) {
		this.vehicle_vin = vehicle_vin;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getDirection() {
		return direction;
	}
	public void setDirection(String direction) {
		this.direction = direction;
	}
	public String getTerminal_time() {
		return terminal_time;
	}
	public void setTerminal_time(String terminal_time) {
		this.terminal_time = terminal_time;
	}
	public String getDingwei_stat() {
		return dingwei_stat;
	}
	public void setDingwei_stat(String dingwei_stat) {
		this.dingwei_stat = dingwei_stat;
	}
    
}
