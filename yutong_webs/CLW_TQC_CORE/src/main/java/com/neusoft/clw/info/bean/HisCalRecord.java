package com.neusoft.clw.info.bean;

import java.util.Date;

public class HisCalRecord {
    private String VEHICLE_VIN; //车辆VIN
    private String CAL_DATE; //计算日期
    private Date END_TIME;    //判断结束日期
    private String TYPE;    //算法类型：0-疲劳驾驶
    
    public String getVEHICLE_VIN() {
        return VEHICLE_VIN;
    }
    public void setVEHICLE_VIN(String vehicle_vin) {
        VEHICLE_VIN = vehicle_vin;
    }
    public String getCAL_DATE() {
        return CAL_DATE;
    }
    public void setCAL_DATE(String cal_date) {
        CAL_DATE = cal_date;
    }
    public Date getEND_TIME() {
        return END_TIME;
    }
    public void setEND_TIME(Date end_time) {
        END_TIME = end_time;
    }
    public String getTYPE() {
        return TYPE;
    }
    public void setTYPE(String type) {
        TYPE = type;
    }
    
}
