package com.neusoft.clw.util.mfqueue;

/**
 * @author Administrator
 */
public class QueueQuotaInfo {
    private String queueName = null;

    private int maxQuota = 0;

    private int nullBlocksQuota = 0;

    private int blocksQuota = 0;

    public String getQueueName() {
        return queueName;
    }

    public void setQueueName(String queueName) {
        this.queueName = queueName;
    }

    public int getMaxQuota() {
        return maxQuota;
    }

    public void setMaxQuota(int maxQuota) {
        this.maxQuota = maxQuota;
    }

    public int getNullBlocksQuota() {
        return nullBlocksQuota;
    }

    public void setNullBlocksQuota(int nullBlocksQuota) {
        this.nullBlocksQuota = nullBlocksQuota;
    }

    public int getBlocksQuota() {
        return blocksQuota;
    }

    public void setBlocksQuota(int blocksQuota) {
        this.blocksQuota = blocksQuota;
    }
}
