package com.neusoft.clw.vncs.domain;

public class AppConfigBean {
	//应用id
	private String app_id;
	//应用名称
	private String app_name;
	//应用ip
	private String app_ip;
	//核心服务请求路径
	private String send_path;
	//核心服务id
	private String core_id;
	//登陆核心服务的密码
	private String core_pass;

	public String getApp_id() {
		return app_id;
	}

	public void setApp_id(String appId) {
		app_id = appId;
	}

	public String getApp_name() {
		return app_name;
	}

	public void setApp_name(String appName) {
		app_name = appName;
	}

	public String getApp_ip() {
		return app_ip;
	}

	public void setApp_ip(String appIp) {
		app_ip = appIp;
	}

	public String getSend_path() {
		return send_path;
	}

	public void setSend_path(String sendPath) {
		send_path = sendPath;
	}

	public String getCore_id() {
		return core_id;
	}

	public void setCore_id(String coreId) {
		core_id = coreId;
	}

	public String getCore_pass() {
		return core_pass;
	}

	public void setCore_pass(String corePass) {
		core_pass = corePass;
	}
}
