package com.neusoft.clw.vncs.domain;

public class CoreCfgBean {
	
	public String core_id;
	public String core_name;
	public String core_ip;
	public String active_time;
	public String active_state;
	public String main_flag;
	public String getCore_id() {
		return core_id;
	}
	public void setCore_id(String coreId) {
		core_id = coreId;
	}
	public String getCore_name() {
		return core_name;
	}
	public void setCore_name(String coreName) {
		core_name = coreName;
	}
	public String getCore_ip() {
		return core_ip;
	}
	public void setCore_ip(String coreIp) {
		core_ip = coreIp;
	}
	public String getActive_time() {
		return active_time;
	}
	public void setActive_time(String activeTime) {
		active_time = activeTime;
	}
	public String getActive_state() {
		return active_state;
	}
	public void setActive_state(String activeState) {
		active_state = activeState;
	}
	public String getMain_flag() {
		return main_flag;
	}
	public void setMain_flag(String mainFlag) {
		main_flag = mainFlag;
	}
}
