package com.neusoft.clw.yw.popup.terminal.ds;

/**
 * 终端信息bean
 * @author JinPeng
 */
public class TerminalBrowseInfo {
    /** 主键ID **/
    private String terminalId = "";

    /** 终端号 **/
    private String terminalCode = "";

    /** 终端厂家名称 **/
    private String oemName = "";

    /** 终端型号名称 **/
    private String typeName = "";

    /** 终端协议名称 **/
    private String protocalName = "";

    /** 终端视频ID **/
    private String videoId = "";
    
    public String getTerminalId() {
        return terminalId;
    }

    public void setTerminalId(String terminalId) {
        this.terminalId = terminalId;
    }

    public String getTerminalCode() {
        return terminalCode;
    }

    public void setTerminalCode(String terminalCode) {
        this.terminalCode = terminalCode;
    }

    public String getOemName() {
        return oemName;
    }

    public void setOemName(String oemName) {
        this.oemName = oemName;
    }

    public String getTypeName() {
        return typeName;
    }

    public void setTypeName(String typeName) {
        this.typeName = typeName;
    }

    public String getProtocalName() {
        return protocalName;
    }

    public void setProtocalName(String protocalName) {
        this.protocalName = protocalName;
    }

    public String getVideoId() {
        return videoId;
    }

    public void setVideoId(String videoId) {
        this.videoId = videoId;
    }
}
