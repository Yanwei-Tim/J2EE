package com.yutong.axxc.tqc.entity.site;

import java.io.Serializable;

import com.fasterxml.jackson.annotation.JsonIgnore;
import com.fasterxml.jackson.annotation.JsonProperty;

public class Area implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -2334847713060761375L;

	@JsonProperty("id")
	private String area_id;
	
	@JsonProperty("name")
	private String area_name;
	
	@JsonProperty("max_lat")
	private String max_lat;
	
	@JsonProperty("min_lat")
	private String min_lat;
	
	@JsonProperty("max_lon")
	private String max_lon;
	
	@JsonProperty("min_lon")
	private String min_lon;
	
	/*@JsonProperty("province")*/
	@JsonIgnore
	private String province;
	
	/*@JsonProperty("city")*/
	@JsonIgnore
	private String city;
	
	

	public String getArea_id() {
		return area_id;
	}

	public void setArea_id(String area_id) {
		this.area_id = area_id;
	}

	public String getArea_name() {
		return area_name;
	}

	public void setArea_name(String area_name) {
		this.area_name = area_name;
	}

	public String getMax_lat() {
		return max_lat;
	}

	public void setMax_lat(String max_lat) {
		this.max_lat = max_lat;
	}

	public String getMin_lat() {
		return min_lat;
	}

	public void setMin_lat(String min_lat) {
		this.min_lat = min_lat;
	}

	public String getMax_lon() {
		return max_lon;
	}

	public void setMax_lon(String max_lon) {
		this.max_lon = max_lon;
	}

	public String getMin_lon() {
		return min_lon;
	}

	public void setMin_lon(String min_lon) {
		this.min_lon = min_lon;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}
	
}
