package com.yutong.axxc.tqc.entity.vehicle;

import java.io.Serializable;

import com.fasterxml.jackson.annotation.JsonProperty;

public class Vehicle implements Serializable {

	private static final long serialVersionUID = -2477404127954687682L;

	/**
	 * 厂区类型
	 */
	@JsonProperty("area_type")
	private String area_type;

	/**
	 * 路线范围
	 */
	@JsonProperty("line_range")
	private String line_range;

	/**
	 * 计划发车时间
	 */
	@JsonProperty("plan_start_time")
	private String plan_start_time;

	/**
	 * vin.
	 */
	@JsonProperty("vehicle_vin")
	private String vehicle_vin;

	/**
	 * 车所在在当前线路ID
	 */
	@JsonProperty("line_id")
	private String line_id;
	
	/**
	 * 车辆编码.
	 */
	@JsonProperty("vehicle_number")
	private String vehicle_code;

	/**
	 * 发车顺序
	 * @return
	 */
	@JsonProperty("sort")
	private String send_order;
	
	
	
	public String getSend_order() {
		return send_order;
	}

	public void setSend_order(String send_order) {
		this.send_order = send_order;
	}

	public String getLine_id() {
		return line_id;
	}

	public void setLine_id(String line_id) {
		this.line_id = line_id;
	}

	public String getVehicle_vin() {
		return vehicle_vin;
	}

	public void setVehicle_vin(String vehicle_vin) {
		this.vehicle_vin = vehicle_vin;
	}

	public String getVehicle_code() {
		return vehicle_code;
	}

	public void setVehicle_code(String vehicle_code) {
		this.vehicle_code = vehicle_code;
	}

	public String getArea_type() {
		return area_type;
	}

	public void setArea_type(String area_type) {
		this.area_type = area_type;
	}

	public String getLine_range() {
		return line_range;
	}

	public void setLine_range(String line_range) {
		this.line_range = line_range;
	}

	public String getPlan_start_time() {
		return plan_start_time;
	}

	public void setPlan_start_time(String plan_start_time) {
		this.plan_start_time = plan_start_time;
	}

}
