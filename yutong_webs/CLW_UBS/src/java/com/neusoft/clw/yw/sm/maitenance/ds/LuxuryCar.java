package com.neusoft.clw.yw.sm.maitenance.ds;

public class LuxuryCar {

    // 高档车编号
    private String luxury_car_id;

    // 车工号
    private String vehicle_number;

    // 车架号
    private String vehicle_vin;

    // 车型
    private String vehicle_type_name;

    // 创建时间
    private String create_time;

    // 修改时间
    private String modify_time;

    // 创建人
    private String creater_id;

    // 最后修改人
    private String modifier_id;

    // 排序字段
    private String sortname;

    // 升序或者降序
    private String sortorder;

    public String getLuxury_car_id() {
        return luxury_car_id;
    }

    public void setLuxury_car_id(String luxury_car_id) {
        this.luxury_car_id = luxury_car_id;
    }

    public String getVehicle_number() {
        return vehicle_number;
    }

    public void setVehicle_number(String vehicle_number) {
        this.vehicle_number = vehicle_number;
    }

    public String getVehicle_vin() {
        return vehicle_vin;
    }

    public void setVehicle_vin(String vehicle_vin) {
        this.vehicle_vin = vehicle_vin;
    }

    public String getVehicle_type_name() {
        return vehicle_type_name;
    }

    public void setVehicle_type_name(String vehicle_type_name) {
        this.vehicle_type_name = vehicle_type_name;
    }

    public String getCreate_time() {
        return create_time;
    }

    public void setCreate_time(String create_time) {
        this.create_time = create_time;
    }

    public String getModify_time() {
        return modify_time;
    }

    public void setModify_time(String modify_time) {
        this.modify_time = modify_time;
    }

    public String getCreater_id() {
        return creater_id;
    }

    public void setCreater_id(String creater_id) {
        this.creater_id = creater_id;
    }

    public String getModifier_id() {
        return modifier_id;
    }

    public void setModifier_id(String modifier_id) {
        this.modifier_id = modifier_id;
    }

    public String getSortname() {
        return sortname;
    }

    public void setSortname(String sortname) {
        this.sortname = sortname;
    }

    public String getSortorder() {
        return sortorder;
    }

    public void setSortorder(String sortorder) {
        this.sortorder = sortorder;
    }

}
