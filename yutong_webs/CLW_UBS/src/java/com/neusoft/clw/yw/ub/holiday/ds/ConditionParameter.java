public class ConditionParameter {

	//节假日类型
	private String day_flag = "";

	//选择年份
	private String year_select = "";

	/**
	 * @return the day_flag
	 */
	public String getDay_flag() {
		return day_flag;
	}

	/**
	 * @param day_flag the day_flag to set
	 */
	public void setDay_flag(String day_flag) {
		this.day_flag = day_flag;
	}

	/**
	 * @return the year_select
	 */
	public String getYear_select() {
		return year_select;
	}

	/**
	 * @param year_select the year_select to set
	 */
	public void setYear_select(String year_select) {
		this.year_select = year_select;
	}
}
