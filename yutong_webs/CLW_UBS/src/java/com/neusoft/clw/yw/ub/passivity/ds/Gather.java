package com.neusoft.clw.yw.ub.passivity.ds;

import java.io.Serializable;

/**
 * 汇总统计DTO
 */
public class Gather implements Serializable
{
    private static final long serialVersionUID = -5055991428255438137L;
    
    private String reportDate;
    private int openEp;// 开通企业
    private int visEp;// 访问企业
    private int vis;// 访问次数
    private float visPercent;// 访问比例
    private String isHoliday;
    
    private String visEpPercent;// 访问企业占比
    private String visCntPercent;// 访问占比
    public String getReportDate()
    {
        return reportDate;
    }
    public void setReportDate(String reportDate)
    {
        this.reportDate = reportDate;
    }
    public int getOpenEp()
    {
        return openEp;
    }
    public void setOpenEp(int openEp)
    {
        this.openEp = openEp;
    }
    public int getVisEp()
    {
        return visEp;
    }
    public void setVisEp(int visEp)
    {
        this.visEp = visEp;
    }
    public int getVis()
    {
        return vis;
    }
    public void setVis(int vis)
    {
        this.vis = vis;
    }
    public float getVisPercent()
    {
        return visPercent;
    }
    public void setVisPercent(float visPercent)
    {
        this.visPercent = visPercent;
    }
    public String getVisEpPercent()
    {
        return visEpPercent;
    }
    public void setVisEpPercent(String visEpPercent)
    {
        this.visEpPercent = visEpPercent;
    }
    public String getVisCntPercent()
    {
        return visCntPercent;
    }
    public void setVisCntPercent(String visCntPercent)
    {
        this.visCntPercent = visCntPercent;
    }
    public String getIsHoliday()
    {
        return isHoliday;
    }
    public void setIsHoliday(String isHoliday)
    {
        this.isHoliday = isHoliday;
    }
    @Override
    public String toString()
    {
        return "Gather [reportDate=" + reportDate + ", openEp=" + openEp
                + ", visEp=" + visEp + ", vis=" + vis + ", visPercent="
                + visPercent + ", isHoliday=" + isHoliday + ", visEpPercent="
                + visEpPercent + ", visCntPercent=" + visCntPercent + "]";
    }
    
  

}
