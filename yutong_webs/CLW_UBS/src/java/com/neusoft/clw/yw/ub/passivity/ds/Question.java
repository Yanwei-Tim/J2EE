package com.neusoft.clw.yw.ub.passivity.ds;

import java.io.Serializable;

/**
 * 汇总统计DTO
 */
public class Question  extends  Gather  implements Serializable  
{
    private static final long serialVersionUID = -5055991428255438137L;
    
    private String id;
    private String name;
    private String pubTime;
    private String endTime;
    public String getId()
    {
        return id;
    }
    public void setId(String id)
    {
        this.id = id;
    }
    public String getName()
    {
        return name;
    }
    public void setName(String name)
    {
        this.name = name;
    }
    public String getPubTime()
    {
        return pubTime;
    }
    public void setPubTime(String pubTime)
    {
        this.pubTime = pubTime;
    }
    public String getEndTime()
    {
        return endTime;
    }
    public void setEndTime(String endTime)
    {
        this.endTime = endTime;
    }
    @Override
    public String toString()
    {
        return "Question [id=" + id + ", name=" + name + ", pubTime=" + pubTime
                + ", endTime=" + endTime +  "]";
    }
    

}
