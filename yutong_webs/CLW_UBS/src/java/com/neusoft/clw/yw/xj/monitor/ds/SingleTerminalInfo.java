package com.neusoft.clw.yw.xj.monitor.ds;

/**
 * 单终端信息bean
 * @author JinPeng
 */
public class SingleTerminalInfo {
    /** 开始时间 **/
    private String startDate = "";

    /** 查询时间段 **/
    private String minuteValue = "";

    /** 车辆VIN **/
    private String vehicleVin = "";

    /** 终端号 **/
    private String terminalCode = "";

    /** SIM卡号 **/
    private String simCardNumber = "";

    /** 监控时间 **/
    private String monitorTime = "";

    /** 视频ID **/
    private String videoId = "";

    /** 车牌号 **/
    private String vehicleLn = "";

    /** 企业名称 **/
    private String enterpriseName = "";

    /** 终端状态 **/
    private String terminalStatus = "";

    /** GPS状态 **/
    private String gpsStatus = "";

    /** GPS状态名 **/
    private String gpsStatusName = "";

    /** 电源状态 **/
    private String powerStatus = "";

    public String getStartDate() {
        return startDate;
    }

    public void setStartDate(String startDate) {
        this.startDate = startDate;
    }

    public String getMinuteValue() {
        return minuteValue;
    }

    public void setMinuteValue(String minuteValue) {
        this.minuteValue = minuteValue;
    }

    public String getVehicleVin() {
        return vehicleVin;
    }

    public void setVehicleVin(String vehicleVin) {
        this.vehicleVin = vehicleVin;
    }

    public String getTerminalCode() {
        return terminalCode;
    }

    public void setTerminalCode(String terminalCode) {
        this.terminalCode = terminalCode;
    }

    public String getSimCardNumber() {
        return simCardNumber;
    }

    public void setSimCardNumber(String simCardNumber) {
        this.simCardNumber = simCardNumber;
    }

    public String getMonitorTime() {
        return monitorTime;
    }

    public void setMonitorTime(String monitorTime) {
        this.monitorTime = monitorTime;
    }

    public String getVideoId() {
        return videoId;
    }

    public void setVideoId(String videoId) {
        this.videoId = videoId;
    }

    public String getVehicleLn() {
        return vehicleLn;
    }

    public void setVehicleLn(String vehicleLn) {
        this.vehicleLn = vehicleLn;
    }

    public String getEnterpriseName() {
        return enterpriseName;
    }

    public void setEnterpriseName(String enterpriseName) {
        this.enterpriseName = enterpriseName;
    }

    public String getTerminalStatus() {
        return terminalStatus;
    }

    public void setTerminalStatus(String terminalStatus) {
        this.terminalStatus = terminalStatus;
    }

    public String getGpsStatus() {
        return gpsStatus;
    }

    public void setGpsStatus(String gpsStatus) {
        this.gpsStatus = gpsStatus;
    }

    public String getGpsStatusName() {
        return gpsStatusName;
    }

    public void setGpsStatusName(String gpsStatusName) {
        this.gpsStatusName = gpsStatusName;
    }

    public String getPowerStatus() {
        return powerStatus;
    }

    public void setPowerStatus(String powerStatus) {
        this.powerStatus = powerStatus;
    }
}
